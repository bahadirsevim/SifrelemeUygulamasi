    public String sezar(String metin, int ks) {
        String alfabe = "abcçdefgğhıijklmnoöprsştuüvyz";
        metin = metin.toLowerCase();
        String sifreliMesaj = "";
        char bosluk = ' ';

        for (int i = 0; i < metin.length(); i++) {
            if (metin.charAt(i) == bosluk) {
                sifreliMesaj += bosluk;
            } else if (metin.charAt(i) == '.') {
                sifreliMesaj += '.';
            } else {
                for (int j = 0; j < 29; j++) {
                    if (metin.charAt(i) == alfabe.charAt(j)) {
                        sifreliMesaj += alfabe.charAt((j + ks) % 29);
                    }
                }
            }
        }
        return sifreliMesaj;
    }
